def check_extreme_risks(city, total_rain_mm, max_temp_c):
    climate = PAK_CLIMATE_DATA.get(city, DEFAULT_CLIMATE)
    alerts = []

    if total_rain_mm >= climate["flood_threshold_mm"]:
        alerts.append(f"🌊 SEVERE FLOOD/WATERLOGGING RISK: {total_rain_mm}mm of rain expected over 5 days.")

    if max_temp_c >= climate["heatwave_c"]:
        alerts.append(f"🔥 HEATWAVE ALERT: Temperatures reaching {max_temp_c}°C. High risk of crop burning and rapid evaporation.")

    today = datetime.now()
    monsoon_date = datetime(today.year, climate["monsoon_start"][0], climate["monsoon_start"][1])
    if today > monsoon_date + timedelta(days=30):
        monsoon_date = datetime(today.year + 1, climate["monsoon_start"][0], climate["monsoon_start"][1])

    days_to_monsoon = (monsoon_date - today).days
    if 0 <= days_to_monsoon <= 5:
        alerts.append(f"⛈️ MONSOON WARNING: Monsoon season begins in {days_to_monsoon} days.")

    return alerts

def fetch_weather_data(city):
    base_url = "http://api.openweathermap.org/data/2.5/forecast"
    params = {"q": city, "appid": OPENWEATHER_API_KEY, "units": "metric"}
    response = requests.get(base_url, params=params)

    if response.status_code != 200:
        return None, None, [], f"Error: Could not retrieve data for '{city}'."

    data = response.json()

    t_data = data['list'][8]
    t_desc = t_data['weather'][0]['description'].title()
    t_temp = t_data['main']['temp']
    tomorrow_summary = f"{t_desc}, Temp: {t_temp}°C"

    weekly_temps = []
    weekly_conds = set()
    total_rain_mm = 0.0
    max_weekly_temp = 0.0

    for item in data['list']:
        temp = item['main']['temp']
        weekly_temps.append(temp)
        weekly_conds.add(item['weather'][0]['description'].title())

        if temp > max_weekly_temp:
            max_weekly_temp = temp

        if 'rain' in item and '3h' in item['rain']:
            total_rain_mm += item['rain']['3h']

    avg_temp = round(sum(weekly_temps) / len(weekly_temps), 1)
    conditions = ", ".join(list(weekly_conds)[:3])
    weekly_summary = f"Avg Temp: {avg_temp}°C, Max Temp: {round(max_weekly_temp, 1)}°C, Total Rain: {round(total_rain_mm, 1)}mm"

    risk_alerts = check_extreme_risks(city, total_rain_mm, max_weekly_temp)

    return tomorrow_summary, weekly_summary, risk_alerts, None

