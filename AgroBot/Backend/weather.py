def automated_12_hour_loop():
    global TARGET_CITY, TARGET_CROP
    wait_time_seconds = 43200

    while True:
        time.sleep(wait_time_seconds)
        timestamp = datetime.now().strftime("%Y-%m-%d %I:%M %p")
        advice = get_ai_farming_advice(TARGET_CITY, TARGET_CROP, context_type="automated")
        new_alert = f"🔔 **AUTOMATED 12-HOUR UPDATE ({timestamp})**\n\n{advice}"
        automated_messages_db.insert(0, new_alert)
        if len(automated_messages_db) > 10:
            automated_messages_db.pop()

threading.Thread(target=automated_12_hour_loop, daemon=True).start()

def read_automated_messages():
    return "\n\n---\n\n".join(automated_messages_db)

def update_preferences(new_city, new_crop):
    global TARGET_CITY, TARGET_CROP
    TARGET_CITY = new_city.strip()
    TARGET_CROP = new_crop
    timestamp = datetime.now().strftime("%I:%M %p")
    system_msg = f"⚙️ *System Update ({timestamp}): Early Warning System now tracking **{TARGET_CROP}** in **{TARGET_CITY}**.*"
    automated_messages_db.insert(0, system_msg)
    return f"✅ Success! Background engine updated."

